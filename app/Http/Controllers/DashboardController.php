     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        //
        $barangkeluar = Barangkeluar::select('nama_bk', Barangkeluar::raw('SUM(jumlah) as brg_keluar'))
                        ->groupBy('nama_bk');

        $barang = Barang::leftJoinSub($barangkeluar, 'barangkeluar', function ($join) {
                $join->on('nama_b', '=', 'nama_bk');
                })
                ->orderby('nama_b', 'asc')->get();

        return view('dashboard.index', compact('barang'));
    }

